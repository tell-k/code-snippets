package main

import "fmt"

func main() {

	var a string = "initial"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var z bool = true
	fmt.Println(z)

	var z2 bool
	fmt.Println(z2)

	var e int
	fmt.Println(e)

	f := "short"
	fmt.Println(f)

	g := true
	fmt.Println(g)
}
